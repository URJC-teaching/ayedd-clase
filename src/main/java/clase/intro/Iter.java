package clase.intro;

public class Iter {
    public static void main(String[] args) {
        int i, a;
        i = 3;

        a = i++;
        System.out.println("a = " + a);
        System.out.println("i = " + i);
        a = ++i;
        System.out.println("a = " + a);
        System.out.println("i = " + i);

    }
}
